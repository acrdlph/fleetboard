class TestHTTPSmoke(ConfigGuard):
    """Spin the real server in DEMO mode (no subprocess) and hit it."""
    def setUp(self):
        super().setUp()
        fb.DEMO = True
        self.srv = ThreadingHTTPServer(("127.0.0.1", 0), fb.Handler)
        self.port = self.srv.server_address[1]
        self.t = threading.Thread(target=self.srv.serve_forever, daemon=True)
        self.t.start()

    def tearDown(self):
        self.srv.shutdown()
        self.srv.server_close()
        super().tearDown()

    def _get(self, path):
        with urllib.request.urlopen(f"http://127.0.0.1:{self.port}{path}", timeout=5) as r:
            return r.status, r.read()

    def test_state_endpoint_json(self):
        status, body = self._get("/api/state")
        self.assertEqual(status, 200)
        self.assertIn("worktrees", json.loads(body))

    def test_index_served(self):
        status, body = self._get("/")
        self.assertEqual(status, 200)
        self.assertIn(b"orchestr", body)

    def test_query_string_route(self):
        status, _ = self._get("/?mission=hello")   # must not 404
        self.assertEqual(status, 200)

    def test_limits_and_topology_endpoints(self):
        self.assertEqual(self._get("/api/limits")[0], 200)
        self.assertEqual(self._get("/api/topology")[0], 200)

    def test_guide_served(self):
        status, body = self._get("/guide")
        self.assertEqual(status, 200)
        self.assertIn(b"closing out a mission", body)

    def test_finish_endpoint_refuses_in_demo(self):
        req = urllib.request.Request(
            f"http://127.0.0.1:{self.port}/api/finish",
            data=json.dumps({"worktree": "orbital-api"}).encode(),
            headers={"Content-Type": "application/json"}, method="POST")
        with urllib.request.urlopen(req, timeout=5) as r:
            d = json.loads(r.read())
        self.assertFalse(d["ok"])
        self.assertIn("demo", d["message"])

    def test_unknown_path_404(self):
        with self.assertRaises(urllib.error.HTTPError) as cm:
            self._get("/nope")
        self.assertEqual(cm.exception.code, 404)
        cm.exception.close()


