class TestSetReserve(ConfigGuard):
    def setUp(self):
        super().setUp()
        self.tmp = tempfile.NamedTemporaryFile("w", suffix=".json", delete=False)
        json.dump({"pattern": "confid", "reserve_percent": {}}, self.tmp)
        self.tmp.close()
        fb.CONFIG_PATH = Path(self.tmp.name)
        fb.CFG["reserve_percent"] = {}
        fb._limits["data"] = None

    def tearDown(self):
        Path(self.tmp.name).unlink(missing_ok=True)
        super().tearDown()

    def test_set_persists_and_preserves_other_keys(self):
        res = fb.set_reserve("main", 20)
        self.assertTrue(res["ok"])
        self.assertEqual(fb.CFG["reserve_percent"], {"main": 20})
        disk = json.loads(Path(self.tmp.name).read_text())
        self.assertEqual(disk["reserve_percent"], {"main": 20})
        self.assertEqual(disk["pattern"], "confid")   # untouched

    def test_zero_removes_entry(self):
        fb.set_reserve("main", 20)
        fb.set_reserve("main", 0)
        self.assertNotIn("main", fb.CFG["reserve_percent"])

    def test_clamps_range(self):
        self.assertEqual(fb.set_reserve("main", 150)["percent"], 95)
        self.assertEqual(fb.set_reserve("main", -5)["percent"], 0)

    def test_rejects_non_numeric(self):
        self.assertFalse(fb.set_reserve("main", "lots")["ok"])


