class TestDispatchLog(ConfigGuard):
    def setUp(self):
        super().setUp()
        self.tmp = tempfile.NamedTemporaryFile("w", suffix=".jsonl", delete=False)
        for i in range(3):
            self.tmp.write(json.dumps({
                "ts": f"2026-07-19T0{i}:00:00", "session": f"mission-s{i}",
                "worktree": f"W{i}", "account": "account2",
                "mission_original": f"do thing {i}"}) + "\n")
        self.tmp.close()
        fb.DISPATCH_LOG = Path(self.tmp.name)
        self._run = fb.run
        fb.run = lambda *a, **k: (0, "mission-s2")   # only s2 is "live" in tmux

    def tearDown(self):
        fb.run = self._run
        Path(self.tmp.name).unlink(missing_ok=True)
        super().tearDown()

    def test_newest_first_and_liveness(self):
        out = fb.read_dispatch_log()
        self.assertEqual(out["entries"][0]["session"], "mission-s2")   # newest first
        self.assertTrue(out["entries"][0]["alive"])                    # matches tmux
        self.assertFalse(out["entries"][1]["alive"])

    def test_limit(self):
        self.assertEqual(len(fb.read_dispatch_log(limit=2)["entries"]), 2)


