class TestMatchWorktree(unittest.TestCase):
    def test_longest_prefix_wins(self):
        prefixes = {"/d/ConfidAI": fb.munge("/d/ConfidAI"),
                    "/d/ConfidAI-security-audit": fb.munge("/d/ConfidAI-security-audit")}
