        self.plain = False           # stand in for a ps that refuses `lstart`

        def run(cmd, cwd=None, timeout=6, env=None):
            if cmd[:2] == ["ps", "-axo"]:
                if self.plain and "lstart=" in cmd[2]:
                    return 1, ""
                return 0, self.ps_out
            return 1, ""
