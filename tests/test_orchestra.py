class TestMatchWorktree(unittest.TestCase):
    def test_longest_prefix_wins(self):
        prefixes = {"/d/ConfidAI": fb.munge("/d/ConfidAI"),
                    "/d/ConfidAI-security-audit": fb.munge("/d/ConfidAI-security-audit")}
        # a project under the audit worktree must not be swallowed by ConfidAI
        proj = fb.munge("/d/ConfidAI-security-audit") + "-backend"
        self.assertEqual(fb.match_worktree(proj, prefixes), "/d/ConfidAI-security-audit")

    def test_exact_and_subdir_match(self):
        prefixes = {"/d/app": fb.munge("/d/app")}
        self.assertEqual(fb.match_worktree(fb.munge("/d/app"), prefixes), "/d/app")
        self.assertEqual(fb.match_worktree(fb.munge("/d/app") + "-sub", prefixes), "/d/app")

    def test_no_match(self):
        prefixes = {"/d/app": fb.munge("/d/app")}
        self.assertIsNone(fb.match_worktree(fb.munge("/d/other"), prefixes))


