class TestClassifySession(unittest.TestCase):
    W = 90  # working_s

    def test_recent_is_working_regardless_of_liveness(self):
        self.assertEqual(fb.classify_session(5, False, [], 0, False, self.W)[0], "working")

    def test_needs_input_on_pending_question(self):
        st, _ = fb.classify_session(200, True, ["AskUserQuestion"], 0, False, self.W)
        self.assertEqual(st, "needs_input")

    def test_pending_workflow_keeps_working(self):
        st, _ = fb.classify_session(3600, True, [], 1, False, self.W)
        self.assertEqual(st, "working")

    def test_skip_perms_tool_run_is_working_not_blocked(self):
        st, tool = fb.classify_session(200, True, ["Bash"], 0, True, self.W)
        self.assertEqual(st, "working")
        self.assertTrue(tool)

    def test_pending_tool_without_skip_perms_is_blocked(self):
        st, _ = fb.classify_session(200, True, ["Bash"], 0, False, self.W)
        self.assertEqual(st, "blocked")

    def test_idle_alive_is_waiting(self):
        self.assertEqual(fb.classify_session(200, True, [], 0, False, self.W)[0], "waiting")

    def test_not_alive_is_ended(self):
        self.assertEqual(fb.classify_session(200, False, [], 0, False, self.W)[0], "ended")

    def test_live_shell_is_working_even_with_idle_transcript(self):
        # the ConfidAI-ci-cleanup case: turn ended ("1 shell still running"),
        # transcript untouched for minutes, but a backgrounded verify build is
        # a live child of the claude process — the agent resumes on its own
        st, tool = fb.classify_session(870, True, [], 0, False, self.W, shells=1)
        self.assertEqual(st, "working")
        self.assertTrue(tool)

    def test_live_shell_beats_blocked(self):
        st, _ = fb.classify_session(200, True, ["Bash"], 0, False, self.W, shells=1)
        self.assertEqual(st, "working")

    def test_pending_question_outranks_live_shell(self):
        st, _ = fb.classify_session(200, True, ["AskUserQuestion"], 0, False, self.W, shells=1)
        self.assertEqual(st, "needs_input")


class TestCloseoutStep(unittest.TestCase):
    """Step two (✕ close) when the landing won't verify — the pure decision
    that breaks the idle-agent / refusing-forever deadlock. One assertion per
    row of the table, plus the 60s boundary exactly at and just under."""

    def test_any_working_refuses_even_if_paired_is_idle(self):
        # a working session may be mid-closeout — never type over it, no matter
        # how the paired session looks or how long ago the brief went out
        self.assertEqual(
            fb.closeout_step("waiting", True, sent=0, now=10_000), "refuse")

    def test_stuck_agent_is_sent_to_chat_not_nudged(self):
        # a nudge typed here would collide with the open question/approval dialog
        self.assertEqual(
            fb.closeout_step("needs_input", False, sent=0, now=10_000), "chat")
        self.assertEqual(
            fb.closeout_step("blocked", False, sent=0, now=10_000), "chat")

    def test_idle_waiting_past_the_guard_gets_nudged(self):
        self.assertEqual(
            fb.closeout_step("waiting", False, sent=0, now=61), "nudge")

    def test_nudge_fires_exactly_at_the_60s_boundary(self):
        self.assertEqual(
            fb.closeout_step("waiting", False, sent=0, now=60), "nudge")

    def test_just_under_60s_refuses_the_double_type(self):
        self.assertEqual(
            fb.closeout_step("waiting", False, sent=0, now=59.9), "refuse")

    def test_scan_miss_never_types_blind(self):
        # no session paired with the live pid -> paired_status is None
        self.assertEqual(
            fb.closeout_step(None, False, sent=0, now=10_000), "refuse")


class TestShellChildren(unittest.TestCase):
    """Attribute Bash-tool wrapper shells to their claude ancestor."""

    # real wrapper shapes observed on macOS (2.1.215), one per variant
    SNAP = ("/bin/zsh -c source /Users/x/.claude-account6/shell-snapshots/"
            "snapshot-zsh-1784.sh 2>/dev/null || true && eval 'sleep 99' "
            "< /dev/null && pwd -P >| /tmp/claude-4632-cwd")
    BARE = ("/bin/zsh -c -l setopt NO_EXTENDED_GLOB NO_BARE_GLOB_QUAL "
            "2>/dev/null || true && eval 'scripts/verify.sh' < /dev/null "
            "&& pwd -P >| /tmp/claude-8a66-cwd")

    def test_counts_wrappers_under_their_claude(self):
        table = {
            100: (1, "??", "claude --dangerously-skip-permissions"),
            101: (100, "??", self.SNAP),
            102: (100, "??", self.BARE),
            200: (1, "??", "claude"),
        }
        self.assertEqual(fb.shell_children(table, {100, 200}), {100: 2})

    def test_plain_shells_and_orphans_do_not_count(self):
        table = {
            100: (1, "??", "claude"),
            101: (100, "??", "/bin/zsh -c ls"),          # not a Bash-tool wrapper
            102: (1, "??", self.SNAP),                   # no claude ancestor
            103: (100, "??", "-zsh"),                    # interactive login shell
        }
        self.assertEqual(fb.shell_children(table, {100}), {})

    def test_walks_intermediate_wrappers(self):
        table = {
            100: (1, "??", "claude"),
            150: (100, "??", "some-wrapper"),
            151: (150, "??", self.BARE),
        }
        self.assertEqual(fb.shell_children(table, {100}), {100: 1})


